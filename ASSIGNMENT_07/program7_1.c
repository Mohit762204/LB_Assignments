#include<stdio.h>

//////////////////////////////////////////////////////////////////////////
//
//  Function Name : Pattern
//  Description :   Displays '$ *' n times.
//  Input :         Int
//  Output :        $   *
//  Author :        Mohit Sandip Zalte
//  Date :          29/10/2025
//
///////////////////////////////////////////////////////////////////////////

void Pattern(
                int iNo                     // Store user Value
            )
{
    int iCnt = 0;

    for(iCnt = 0; iCnt < iNo; iCnt++)
    {
        printf(" $ * ");
    }
}   // End of Pattern()

//////////////////////////////////////////////////////////////////////////
//
//  Main Function
//
//////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;                         // To Store user input
    
    printf("Enter Number :");
    scanf("%d",&iValue);

    pattern(iValue);

    return 0 ;
}   // End of main()

