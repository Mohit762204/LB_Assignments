#include<stdio.h>

//////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description :   Display numbers Till N
//  Input :         integer
//  Output :        numbers
//  Author :        Mohit Sandip Zalte
//  Date :          29/10/2025
//
///////////////////////////////////////////////////////////////////////////

void Display(
                int iNo                     // Store user Value
            )
{
    int iCnt = 0;                           // Counter

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        printf(" %d ",iCnt);
    }
}   // End of Display()

//////////////////////////////////////////////////////////////////////////
//
//  Main Function
//
//////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;                         // To Store user input

    printf("Enter Number :");
    scanf("%d",&iValue);

    Display(iValue);                        // Function call

    return 0 ;
}   // End of main()

