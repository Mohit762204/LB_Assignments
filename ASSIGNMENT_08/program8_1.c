#include<stdio.h>

/////////////////////////////////////////////////////////////////////////
//
//  Function Name : Number
//  Description   : Displays Small / Medium / Large based on input number.
//  Input         : Integer (iNo)
//  Output        : Prints category on screen
//  Author        : Mohit Sandip Zalte
//  Date          : 29/10/2025
//
/////////////////////////////////////////////////////////////////////////

void Number(int iNo)
{
    if(iNo <= 50)
    {
        printf("Small\n");
    }
    else if(iNo <= 100)
    {
        printf("Medium\n");
    }
    else
    {
        printf("Large\n");
    }
}   // End of Number()

/////////////////////////////////////////////////////////////////////////
//
//                            Main Function
//
/////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;     // Number to categorize

    printf("Enter Number : ");
    scanf("%d", &iValue);

    Number(iValue);

    return 0;
}   // End of main()

